  module subroutine train( &
       this, input, output, num_epochs, batch_size, &
       plateau_threshold, shuffle_batches, batch_print_step, verbose &
  )
    !! Train the network
    !!
    !! This function trains the network on the input data for a number of
    !! epochs. The input data is split into batches of size batch_size and
    !! the network is trained on each batch. The network is trained using
    !! the optimiser specified in the network object.
    use athena__tools_infile, only: stop_check
    implicit none

    ! Arguments
    class(network_type), intent(inout) :: this
    !! Instance of network
    class(*), dimension(..), intent(in) :: input
    !! Input data
    class(*), dimension(:,:), intent(in) :: output
    !! Output data
    integer, intent(in) :: num_epochs
    !! Number of epochs
    integer, optional, intent(in) :: batch_size ! deprecated
    !! Batch size
    real(real32), optional, intent(in) :: plateau_threshold
    !! Plateau threshold
    logical, optional, intent(in) :: shuffle_batches
    !! Shuffle batches
    integer, optional, intent(in) :: batch_print_step
    !! Batch print step
    integer, optional, intent(in) :: verbose
    !! Verbosity level

    ! Training parameters
    real(real32) :: batch_loss, batch_accuracy, avg_loss, avg_accuracy
    !! Loss and accuracy

    ! learning parameters
    integer :: l, num_samples, s_idx
    !! Loop index
    integer :: num_batches
    !! Number of batches
    integer :: converged
    !! Convergence flag
    integer :: window_width
    !! Length of convergence check window
    integer :: verbose_
    !! Verbosity level
    integer :: batch_print_step_
    !! Batch print step
    real(real32) :: plateau_threshold_
    !! Plateau threshold
    logical :: shuffle_batches_
    !! Shuffle batches

    ! Training loop variables
    integer :: epoch, batch, start_index, end_index
    !! Loop index
    integer, allocatable, dimension(:) :: batch_order
    !! Batch order

    integer :: i, s, time, time_old, clock_rate
    !! Loop index

    class(*), allocatable :: data_poly(:,:)

#ifdef _OPENMP
    type(network_type) :: this_copy
    !! Copy of network
#endif
    !  integer :: timer_start = 0, timer_stop = 0, timer_sum = 0, timer_tot = 0
    !  integer :: forward_timer = 0, backward_timer = 0, update_timer = 0


    !---------------------------------------------------------------------------
    ! Check loss and accuracy methods are set
    !---------------------------------------------------------------------------
    if(.not.allocated(this%loss))then
       call stop_program("loss method not set")
       return
    end if
    if(.not.associated(this%get_accuracy))then
       call stop_program("accuracy method not set")
       return
    end if


    !---------------------------------------------------------------------------
    ! Initialise optional arguments
    !---------------------------------------------------------------------------
    verbose_ = 0
    batch_print_step_ = 20
    plateau_threshold_ = 1.E-2_real32
    shuffle_batches_ = .true.
    if(present(plateau_threshold)) plateau_threshold_ = plateau_threshold
    if(present(shuffle_batches)) shuffle_batches_ = shuffle_batches
    if(present(batch_print_step)) batch_print_step_ = batch_print_step
    if(present(verbose)) verbose_ = verbose
    if(present(batch_size)) this%batch_size = batch_size


    !---------------------------------------------------------------------------
    ! Initialise monitoring variables
    !---------------------------------------------------------------------------
    window_width = max(ceiling(500._real32/this%batch_size),1)
    do i = 1, size(this%metrics,dim=1)
       this%metrics(i)%window_width = window_width
    end do


    !---------------------------------------------------------------------------
    ! If parallel, initialise slices
    !---------------------------------------------------------------------------
    select type(output)
    type is(graph_type)
       num_batches = size(output,dim=2) / this%batch_size
    class is(array_type)
       if(this%use_graph_output)then
          num_batches = size(output,dim=2) / this%batch_size
       else
          num_batches = size(output(1,1)%val,dim=2) / this%batch_size
       end if
    class default
       num_batches = size(output,dim=2) / this%batch_size
    end select
    allocate(batch_order(num_batches))
    do batch = 1, num_batches
       batch_order(batch) = batch
    end do


    !---------------------------------------------------------------------------
    ! Get number of samples
    !---------------------------------------------------------------------------
    num_samples = this%save_input( input )
    if(size(output,2).ne.num_samples.and.this%use_graph_output)then
       call stop_program("number of samples in input and output do not match")
       return
    elseif(size(output,1).ne.this%num_outputs.and..not.this%use_graph_input)then
       call stop_program("number of outputs in output does not match network")
       return
    end if


    !---------------------------------------------------------------------------
    ! Set/reset batch size for training
    !---------------------------------------------------------------------------
    call this%set_batch_size(this%batch_size)


    !---------------------------------------------------------------------------
    ! Turn off inference booleans
    !---------------------------------------------------------------------------
    do l = 1, this%num_layers
       this%model(l)%layer%inference = .false.
    end do


    !---------------------------------------------------------------------------
    ! Query system clock
    !---------------------------------------------------------------------------
    call system_clock(time, count_rate = clock_rate)


    epoch_loop: do epoch = 1, num_epochs
       this%epoch = epoch
       !------------------------------------------------------------------------
       ! Shuffle batch order at the start of each epoch
       !------------------------------------------------------------------------
       if(shuffle_batches_)then
          call shuffle(batch_order)
       end if

       avg_loss     = 0._real32
       avg_accuracy = 0._real32

       !------------------------------------------------------------------------
       ! Batch loop
       ! ... split data up into minibatches for training
       !------------------------------------------------------------------------
       batch_loop: do batch = 1, num_batches


          ! Set batch start and end index
          !---------------------------------------------------------------------
          start_index = (batch_order(batch) - 1) * this%batch_size + 1
          end_index = batch_order(batch) * this%batch_size


          ! Forward pass
          !---------------------------------------------------------------------
          !  call system_clock(timer_start)
          select case(this%use_graph_input)
          case(.true.)
             data_poly = get_sample_graph( &
                  this%input_graph, start_index, end_index, this%batch_size &
             )
          case default
             data_poly = get_sample_array( &
                  this%input_array, start_index, end_index, this%batch_size, &
                  as_graph = .false. &
             )
          end select
          call this%forward_generic2d(data_poly)
          call this%model(this%leaf_vertices(1))%layer%output(1,1)%backward()
          deallocate(data_poly)
          !  call system_clock(timer_stop)
          !  forward_timer = forward_timer + timer_stop - timer_start


          ! Backward pass and store predicted output
          !---------------------------------------------------------------------
          !  call system_clock(timer_start)
          select type(output)
          type is(graph_type)
             data_poly = get_sample_graph( &
                  output, start_index, end_index, this%batch_size &
             )
          type is(array_type)
             data_poly = get_sample_array( &
                  output, start_index, end_index, this%batch_size, &
                  as_graph = this%use_graph_output &
             )
          end select
          call this%backward_generic2d(data_poly)
          deallocate(data_poly)
          !  call system_clock(timer_stop)
          !  backward_timer = backward_timer + timer_stop - timer_start


          ! Compute loss and accuracy (for monitoring)
          !---------------------------------------------------------------------
          batch_loss = this%calc_output_loss(output, start_index, end_index)
          batch_accuracy = this%calc_output_accuracy(output, start_index, end_index)


          ! Average metric over batch size and store
          ! Check metric convergence
          !---------------------------------------------------------------------
          avg_loss = avg_loss + batch_loss
          avg_accuracy = avg_accuracy + batch_accuracy
          call this%metrics(1)%append(batch_loss / this%batch_size)
          call this%metrics(2)%append(batch_accuracy / this%batch_size)
          do i = 1, size(this%metrics,dim=1)
             call this%metrics(i)%check(plateau_threshold_, converged)
             if(converged.ne.0)then
                exit epoch_loop
             end if
          end do


          ! Update weights and biases using optimization algorithm
          !---------------------------------------------------------------------
          ! call system_clock(timer_start)
          call this%update()
          ! call system_clock(timer_stop)
          ! update_timer = update_timer + timer_stop - timer_start


          ! Print batch results
          !---------------------------------------------------------------------
          if(abs(verbose_).gt.0.and.&
               (batch.eq.1.or.abs(mod(batch,batch_print_step_)).lt.1.E-6))then
             write(6,'("epoch=",I0,", batch=",I0,&
                  &", learning_rate=",F0.3,", loss=",F0.3,", accuracy=",F0.3)' &
             ) &
                  this%epoch, batch, &
                  this%optimiser%lr_decay%get_lr( &
                       this%optimiser%learning_rate, this%optimiser%iter &
                  ), &
                  avg_loss/(batch*this%batch_size), &
                  avg_accuracy/(batch*this%batch_size)
          end if


          ! Time check
          !---------------------------------------------------------------------
          if(verbose_.eq.-2)then
             time_old = time
             call system_clock(time)
             write(*,'("time check: ",F5.3," seconds")') &
                  real(time-time_old)/clock_rate
             time_old = time
          end if


          ! Check for user-name stop file
          !---------------------------------------------------------------------
          if(stop_check())then
             write(0,*) "STOPCAR ENCOUNTERED"
             write(0,*) "Exiting training loop..."
             exit epoch_loop
          end if

       end do batch_loop


       ! Print epoch summary results
       !------------------------------------------------------------------------
       if(verbose_.eq.0)then
          write(6,'("epoch=",I0,&
               &", learning_rate=",F0.3,", val_loss=",F0.3,&
               &", val_accuracy=",F0.3)' &
          ) &
               this%epoch, &
               this%optimiser%lr_decay%get_lr( &
                    this%optimiser%learning_rate, this%optimiser%iter &
               ), &
               this%metrics(1)%val, this%metrics(2)%val
       end if


    end do epoch_loop

    ! write(*,*) "forward timer: ", real(forward_timer)/clock_rate
    ! write(*,*) "backward timer: ", real(backward_timer)/clock_rate
    ! write(*,*) "update timer: ", real(update_timer)/clock_rate

  end subroutine train
