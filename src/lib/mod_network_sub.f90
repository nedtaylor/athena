  module subroutine set_batch_size(this, batch_size)
    !! Set the batch size for the network
    implicit none

    ! Arguments
    class(network_type), intent(inout) :: this
    !! Instance of network
    integer, intent(in) :: batch_size
    !! Batch size

    ! Local variables
    integer :: l
