  module subroutine backward_mixed(this, output)
    !! Backward pass for real output
    implicit none

    ! Arguments
    class(network_type), intent(inout) :: this
    !! Instance of network
    type(array2d_type), dimension(:,:), intent(in) :: output
    !! Output

    ! Local variables
    integer :: i, s
    !! Loop index
    integer :: input_idx
