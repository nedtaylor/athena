  subroutine convert_polymorphic_to_array2d( &
       input, output_array, output_graph, num_samples, num_input_layers &
  )
    !! Convert polymorphic input to array2d
    implicit none

    ! Arguments
    class(*), dimension(..), intent(in) :: input
    !! Input
    type(array2d_type), dimension(:), allocatable, intent(out) :: output_array
    !! Output
    type(graph_type), dimension(:,:), allocatable, intent(out) :: output_graph
    !! Output
    integer, intent(out) :: num_samples
    !! Number of samples
    integer, intent(in) :: num_input_layers
    !! Number of input layers

    ! Local variables
    integer :: i, input_rank, num_inputs
    !! Loop index
    logical :: l_valid_rank_type
    !! Boolean whether rank type is valid
    character(256) :: err_msg
    !! Error message


    ! Determine the rank of the input
    !---------------------------------------------------------------------------
    select rank(input)
    rank(0)
    rank(1)
    rank default
       input_rank = rank(input)
       num_samples = size(input, input_rank)
       num_inputs = size(input) / num_samples
       allocate(output_array(1))
       call output_array(1)%allocate(array_shape=[num_inputs, num_samples])
    end select
    l_valid_rank_type = .false.


    ! Process input based on its rank
    !---------------------------------------------------------------------------
    rank_select: select rank(input)
    rank(0)
       select type(input)
       type is(real); exit rank_select
       class default; l_valid_rank_type = .true.
       end select
       if(num_input_layers.ne.1)then
          call stop_program( &
               "number of input arrays does not match expected number of &
               &input layers" &
          )
          return
       end if
       allocate(output_array(1))
       num_samples = get_num_samples(input)
       select type(input)
       class is(array_type)
          call handle_array_type(input, output_array(i), num_samples)
       type is(array_container_type)
          call handle_array_type(input%array, output_array(i), num_samples)
       end select
    rank(1)
       select type(input)
       type is(real)
          exit rank_select
       type is(graph_type)
          num_samples = size(input, dim=1)
          allocate(output_graph(num_samples, num_input_layers))
          output_graph(:,1) = input(:)
          return
       class default
          l_valid_rank_type = .true.
       end select
       if(size(input,1).ne.num_input_layers)then
          call stop_program( &
               "number of input arrays does not match expected number of &
               &input layers" &
          )
          return
       end if
       allocate(output_array(size(input,1)))
       num_samples = get_num_samples(input(1))
       do i = 1, size(input,1)
          select type(input)
          class is(array_type)
             call handle_array_type(input(i), output_array(i), num_samples)
          type is(array_container_type)
             call handle_array_type( &
                  input(i)%array, output_array(i), num_samples &
             )
          end select
       end do
    rank(2)
       select type(input)
       type is(real)
          output_array(1)%val = reshape(input, [num_inputs, num_samples])
          l_valid_rank_type = .true.
       type is(graph_type)
          num_samples = size(input, dim=2)
          allocate(output_graph(num_input_layers, num_samples))
          output_graph(:,:) = input(:,:)
          return
       end select
    rank(3)
       select type(input)
       type is(real)
          output_array(1)%val = reshape(input, [num_inputs, num_samples])
          l_valid_rank_type = .true.
       end select
    rank(4)
       select type(input)
       type is(real)
          output_array(1)%val = reshape(input, [num_inputs, num_samples])
          l_valid_rank_type = .true.
       end select
    rank(5)
       select type(input)
       type is(real)
          output_array(1)%val = reshape(input, [num_inputs, num_samples])
          l_valid_rank_type = .true.
       end select
    end select rank_select

    if(.not.l_valid_rank_type)then
       write(err_msg,'("Unknown input type for rank ",I0)') input_rank
       call stop_program(err_msg)
       return
    end if

  contains

    function get_num_samples(input) result(num_samples)
      !! Get the number of samples in the input

      ! Arguments
      class(*), intent(in) :: input
      !! Input
      integer :: num_samples
      !! Number of samples

      select type(input)
      class is(array_type)
         num_samples = size(input%val, 2)
      class is(array_container_type)
         num_samples = size(input%array%val, 2)
      class default
         call stop_program("Unknown input type in get_num_samples")
         return
      end select
    end function get_num_samples


    subroutine handle_array_type(input, output, num_samples)
      !! Handle array type input

      ! Arguments
      class(array_type), intent(in) :: input
      !! Input
      type(array2d_type), intent(out) :: output
      !! Output
      integer, intent(in) :: num_samples
      !! Number of samples

      if(size(input%val,2).ne.num_samples)then
         call stop_program("number of samples in input arrays do not match")
         return
      end if
      call output%allocate( array_shape = &
           [ product(input%shape(1:input%rank)), num_samples ] &
      )
      output%val = input%val
    end subroutine handle_array_type

  end subroutine convert_polymorphic_to_array2d
