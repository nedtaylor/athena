    !! Get learnable parameters
    implicit none

    ! Arguments
    class(network_type), intent(in) :: this
    !! Instance of network
    real(real32), dimension(this%num_params) :: params
    !! Parameters

    ! Local variables
    integer :: l, start_idx, end_idx
