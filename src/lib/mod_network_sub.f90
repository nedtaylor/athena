  pure module function get_gradients(this) result(gradients)
    !! Get gradients
    implicit none

    ! Arguments
    class(network_type), intent(in) :: this
    !! Instance of network
    real(real32), dimension(this%num_params) :: gradients
    !! Gradients

    ! Local variables
